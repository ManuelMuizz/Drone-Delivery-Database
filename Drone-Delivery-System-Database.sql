-----DDL Queries for creating database tables-----
--Create customers table--
CREATE TABLE IFS244A2.CUSTOMERS(
CUSTOMER_ID NUMBER(10) NOT NULL, 
CUSTOMER_FIRST_NAME VARCHAR(50) NOT NULL,
CUSTOMER_LAST_NAME VARCHAR(50) NOT NULL,
CUSTOMER_EMAIL VARCHAR(25) NOT NULL,
CUSTOMER_PHONE VARCHAR(10) NOT NULL,
CUSTOMER_DELIVERY_ADDRESS VARCHAR (120) NOT NULL,
CONSTRAINT ifs244a2_cust_custid_pk PRIMARY KEY(CUSTOMER_ID),
CONSTRAINT ifs244a2_cust_email_uk UNIQUE(CUSTOMER_EMAIL),
CONSTRAINT ifs244a2_cust_phone_uk UNIQUE(CUSTOMER_PHONE)
);

--Create categories table--
CREATE TABLE IFS244A2.CATEGORIES(
CATEGORY_ID NUMBER(10) NOT NULL,
CATEGORY_NAME VARCHAR(50) NOT NULL,
CATEGORY_DESCRIPTION VARCHAR(120) NOT NULL,
CONSTRAINT ifs244a2_cat_catid_pk PRIMARY KEY(CATEGORY_ID)
);

--Create suppliers table--
CREATE TABLE IFS244A2.SUPPLIERS(
SUPPLIER_ID NUMBER(10) NOT NULL, 
SUPPLIER_NAME VARCHAR(50) NOT NULL,
SUPPLIER_EMAIL VARCHAR(25) NOT NULL, 
SUPPLIER_PHONE VARCHAR(10) NOT NULL, 
SUPPLIER_LOCATION VARCHAR(25) NOT NULL,
CATEGORY_ID NUMBER(10) NOT NULL,
CONSTRAINT ifs244a2_sup_supid_pk PRIMARY KEY(SUPPLIER_ID),
CONSTRAINT ifs244a2_sup_email_uk UNIQUE(SUPPLIER_EMAIL),
CONSTRAINT ifs244a2_sup_phone_uk UNIQUE(SUPPLIER_PHONE),
CONSTRAINT ifs244a2_cat_catid_fk FOREIGN KEY (CATEGORY_ID) REFERENCES ifs244a2.categories (CATEGORY_ID)
);

--Create products table--
CREATE TABLE IFS244A2.PRODUCTS(
PRODUCT_ID NUMBER(10) NOT NULL, 
PRODUCT_NAME VARCHAR(50) NOT NULL, 
PRODUCT_DESCRIPTION VARCHAR(120) NOT NULL, 
PRODUCT_PRICE NUMBER(8,2) NOT NULL,--recorded in Rand amounts
PRODUCT_SHIPPING_WEIGHT NUMBER(8,2) NOT NULL, --recorded in kg's
CATEGORY_ID NUMBER(10) NOT NULL,
SUPPLIER_ID NUMBER(10) NOT NULL,
CONSTRAINT ifs244a2_prod_prodid_pk PRIMARY KEY (PRODUCT_ID),
CONSTRAINT ifs244a2_cat_catid_fk2 FOREIGN KEY(CATEGORY_ID) REFERENCES ifs244a2.categories (CATEGORY_ID),
CONSTRAINT ifs244a2_sup_supid_fk2 FOREIGN KEY(SUPPLIER_ID) REFERENCES ifs244a2.suppliers (SUPPLIER_ID)
);

--Create drones table--
CREATE TABLE IFS244A2.DRONES(
DRONE_ID NUMBER(10) NOT NULL, 
DRONE_NAME VARCHAR(50) NOT NULL,
DRONE_REG_NUMBER VARCHAR(10) NOT NULL, 
DRONE_TYPE VARCHAR(50) NOT NULL,
DRONE_PAYLOAD_KG NUMBER(8,2) NOT NULL, --recorded in kg's
DRONE_RANGE_KM NUMBER(8,2) NOT NULL, --recorded in km's
DRONE_ENDURANCE_HRS NUMBER(8) NOT NULL, --recorded as number of hours
IN_SERVICE_SINCE DATE NOT NULL,
RETIRES_AT DATE NOT NULL,
CONSTRAINT ifs244a2_drone_droneid_pk PRIMARY KEY(DRONE_ID),
CONSTRAINT ifs244a2_drone_drn_uk UNIQUE(DRONE_REG_NUMBER)
);

--Create orders table--
CREATE TABLE IFS244A2.ORDERS(
ORDER_ID NUMBER(10) NOT NULL, 
CUSTOMER_EMAIL VARCHAR(25) NOT NULL,
CUSTOMER_DELIVERY_ADDRESS VARCHAR(120) NOT NULL,
ORDER_DATE DATE NOT NULL,
CONSTRAINT ifs244a2_ord_ordid_pk PRIMARY KEY(ORDER_ID),
CONSTRAINT ifs244a2_cust_email_fk FOREIGN KEY(CUSTOMER_EMAIL) REFERENCES ifs244a2.customers (CUSTOMER_EMAIL)
);

--Create order details table--
CREATE TABLE IFS244A2.ORDERDETAILS(
ORDER_DETAILS_ID NUMBER(10) NOT NULL, 
ORDER_ID NUMBER(10) NOT NULL, 
PRODUCT_ID NUMBER(10) NOT NULL, 
PRODUCT_NAME VARCHAR(50) NOT NULL,
PRODUCT_PRICE NUMBER(8,2) NOT NULL,--recorded in Rand amounts
PRODUCT_QUANTITY NUMBER(8) NOT NULL,
ORDER_SHIPPING_WEIGHT NUMBER(8,2) NOT NULL,--recorded in kg's
ORDER_TOTAL NUMBER(8,2) NOT NULL, --recorded in Rand amounts
DELIVERY_FEE NUMBER(8,2) NOT NULL,--recorded in Rand amounts
ORDER_SUB_TOTAL NUMBER(8,2) NOT NULL,--recorded in Rand amounts
CONSTRAINT ifs244a2_orddet_ordetid_pk PRIMARY KEY(ORDER_DETAILS_ID),
CONSTRAINT ifs244a2_ord_ordid_fk FOREIGN KEY(ORDER_ID) REFERENCES ifs244a2.orders (ORDER_ID),
CONSTRAINT ifs244a2_prod_prodid_fk FOREIGN KEY(PRODUCT_ID) REFERENCES ifs244a2.products (PRODUCT_ID)
);

--Create deliveries table--
CREATE TABLE IFS244A2.DELIVERIES(
DELIVERY_ID NUMBER(10) NOT NULL,
ORDER_ID NUMBER(10) NOT NULL,
ORDER_DETAILS_ID NUMBER(10) NOT NULL,
CUSTOMER_DELIVERY_ADDRESS VARCHAR(120) NOT NULL,
ORDER_SHIPPING_WEIGHT NUMBER(8,2) NOT NULL,--recorded in kg's
DRONE_ID NUMBER(10) NOT NULL,
SCHEDULED_DELIVERY_DATE DATE NOT NULL,
DELIVERY_STATUS VARCHAR(25) NOT NULL,
ACTUAL_DELIVERY_DATE DATE,
DELIVERY_COMMENTS VARCHAR(120), 
CONSTRAINT ifs242a2_deliv_delivid_pk PRIMARY KEY(DELIVERY_ID),
CONSTRAINT ifs242a2_ord_ordid_fk FOREIGN KEY (ORDER_ID) REFERENCES ifs244a2.orders (ORDER_ID),
CONSTRAINT ifs244a2_orddet_ordetid_fk FOREIGN KEY (ORDER_DETAILS_ID) REFERENCES ifs244a2.orderdetails (ORDER_DETAILS_ID),
CONSTRAINT ifs244a2_drone_droneid_fk FOREIGN KEY (DRONE_ID) REFERENCES ifs244a2.drones (DRONE_ID)
);

